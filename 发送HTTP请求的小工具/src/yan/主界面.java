    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        try {
            JFileChooser jfc=new JFileChooser(".");
            if(jfc.showOpenDialog(this)==JFileChooser.APPROVE_OPTION){
                root=new DefaultMutableTreeNode();
                f=jfc.getSelectedFile();
                jInternalFrame文件.setTitle(f.getName());
                DocumentBuilder db=DocumentBuilderFactory.newInstance().newDocumentBuilder();
                Document dom=db.parse(f); 
                for(int i=0;i<dom.getChildNodes().getLength();i++){ 
                    Node n=dom.getChildNodes().item(i);
                    if(n.getNodeType()==Node.ELEMENT_NODE){
                        for(int j=0;i<n.getChildNodes().getLength();j++){ 
                            if(n.getChildNodes().item(j)==null)break ;
                            if(n.getChildNodes().item(j).getNodeType()==Node.ELEMENT_NODE){
                                root.add(new 请求记录(n.getChildNodes().item(j)));  
                            }
                        } 
                        break;
                    }
                } 
                DefaultTreeModel m=(DefaultTreeModel)jTree1.getModel();
                m.setRoot(root);
                jTree1.expandRow(0);
                jTree1.updateUI();
            } 
        } catch (ParserConfigurationException | SAXException | IOException ex) {
            Logger.getLogger(getClass().getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jMenuItem6ActionPerformed

