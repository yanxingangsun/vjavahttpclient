    private void jTree1ValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTree1ValueChanged
        if(jTree1.getSelectionPath()==null)return ;
        请求记录 node=(请求记录)jTree1.getSelectionPath().getLastPathComponent();
        if(node!=null){
            this.jTextField说明.setText(node.请求说明);
            this.jTextArea地址.setText(node.请求地址);
            DefaultTableModel m=(DefaultTableModel)this.jTable参数.getModel();
            m.setRowCount(0);
            for(String s:node.请求参数.keySet())
                m.addRow(new String[]{s,node.请求参数.get(s)});
        }
    }//GEN-LAST:event_jTree1ValueChanged

    private void jMenuItem7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem7ActionPerformed
        if(f==null)return ;
        
        请求记录 记录=(请求记录)this.jTree1.getSelectionPath().getLastPathComponent();
        记录.保存(this);
        
        FileOutputStream fos=null;
        try {
            fos = new FileOutputStream(f);
            String s="<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n";
            s+="<请求集合>\n";
            for(int i=0;i<root.getChildCount();i++){
                请求记录 n=(请求记录)root.getChildAt(i);
                s+=n.toXMLString();
            }   s+="</请求集合>";
            fos.write(s.getBytes());
            fos.flush(); 
            
            jTree1.updateUI();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(主界面.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(主界面.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fos.close();
            } catch (IOException ex) {
                Logger.getLogger(主界面.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
    }//GEN-LAST:event_jMenuItem7ActionPerformed

